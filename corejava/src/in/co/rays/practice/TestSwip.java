package in.co.rays.practice;

public class TestSwip {
	public static void main(String[] args) {
		
		
		int a = 20;
		int b = 10;
		
		a = a + b;    //  20 + 10 = 30
		
		b = a - b;     //  30 -  10 = 20
		
		a = a - b;
		
		System.out.println( "a = " + a );
		
		System.out.println("b = " + b );
		
		
				
	}

}
