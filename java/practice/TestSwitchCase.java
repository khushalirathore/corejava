package core.java.practice;

public class TestSwitchCase {
	public static void main(String[] args) {
	
		for (int i = 1; i <= 2; i++) {
			
		
		 switch (i) {
		case 1:
			
			System.out.println("case 1 ");
			break;

		case 2: 
			System.out.println("case 2");
			break;
			
		default: 
			
			System.out.println("invalid case");
			break;
		 }
		 }
	}

}
