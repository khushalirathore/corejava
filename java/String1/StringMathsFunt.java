package core.java.String1;

public class StringMathsFunt {
	public static void main(String[] args) {
		
		System.out.println("max 2 , 5 = "+  Math.max(2, 5));
		
		System.out.println("min 5 ,9 = " +Math.min(5, 9));
		
		System.out.println("absolute 5.7 = " + Math.abs(5.7));
		
		System.out.println(" exp 10 = " + Math.exp(10));
		
		System.out.println("random number = " + Math.random());
		
		System.out.println(" square root = " + Math.sqrt(9));
		
	}

}
